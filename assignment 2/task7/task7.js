
var a = Number(prompt("enter a number"));
var b = Number(prompt("enter number"));
var c = prompt("use arithmatic operater +,-,*,/,%");
if(c=="+") {
    console.log(a+b)
}
else if(c=="/") {
    console.log(a/b)
}
else if(c=="-") {
    console.log(a-b)
}
else if(c=="*") {
    console.log(a*b)
}
else if(c=="%") {
    console.log(a%b)
}
